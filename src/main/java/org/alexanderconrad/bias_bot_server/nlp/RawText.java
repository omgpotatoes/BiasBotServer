package org.alexanderconrad.bias_bot_server.nlp;

public class RawText {

  private String rawText;

  public RawText() {
    super();
    this.rawText = "";
  }

  public RawText(String rawText) {
    super();
    this.rawText = rawText;
  }

  public String getRawText() {
    return rawText;
  }

  public void setRawText(String rawText) {
    this.rawText = rawText;
  }

}
